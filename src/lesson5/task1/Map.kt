/**
 * Сложная
 *
 * Для заданного списка неотрицательных чисел и числа определить,
 * есть ли в списке пара чисел таких, что их сумма равна заданному числу.
 * Если да, верните их индексы в виде Pair<Int, Int>;
 * если нет, верните пару Pair(-1, -1).
 *
 * Индексы в результате должны следовать в порядке (меньший, больший).
 *
 * Постарайтесь сделать ваше решение как можно более эффективным,
 * используя то, что вы узнали в данном уроке.
 *
 * Например:
 *   findSumOfTwo(listOf(1, 2, 3), 4) -> Pair(0, 2)
 *   findSumOfTwo(listOf(1, 2, 3), 6) -> Pair(-1, -1)
 */
fun findSumOfTwo(list: List<Int>, number: Int): Pair<Int, Int> {
    val numbers = IntArray(number + 1) { -1 }
    if ((number % 2 == 0) && (number / 2 in list)) {
        val pair = mutableListOf<Int>()
        for (i in 0 until list.size) {
            if (list[i] == number / 2) pair += i
        }
        if (pair.size > 1) return Pair(pair[0], pair[1])
    }
    for (i in 0 until number + 1) {
        if (i in list) numbers[i] = list.indexOf(i)
    }
    for (i in 0 until number) {
        if ((numbers[i] != -1) && (numbers[number - i] != -1) && (numbers[i] != numbers[number - i]))
            return Pair(minOf(numbers[i], numbers[number - i]), maxOf(numbers[i], numbers[number - i]))
    }
    return Pair(-1, -1)
}

/**
 * Очень сложная
 *
 * Входными данными является ассоциативный массив
 * "название сокровища"-"пара (вес сокровища, цена сокровища)"
 * и вместимость вашего рюкзака.
 * Необходимо вернуть множество сокровищ с максимальной суммарной стоимостью,
 * которые вы можете унести в рюкзаке.
 *
 * Перед решением этой задачи лучше прочитать статью Википедии "Динамическое программирование".
 *
 * Например:
 *   bagPacking(
 *     mapOf("Кубок" to (500 to 2000), "Слиток" to (1000 to 5000)),
 *     850
 *   ) -> setOf("Кубок")
 *   bagPacking(
 *     mapOf("Кубок" to (500 to 2000), "Слиток" to (1000 to 5000)),
 *     450
 *   ) -> emptySet()
 */
fun bagPacking(treasures: Map<String, Pair<Int, Int>>, capacity: Int): Set<String> {
    val answer = mutableSetOf<String>()
    val treasuresList = treasures.toList()
    val whole: Array<Array<Int>> = Array(treasures.size + 1) { Array(capacity + 1) { 0 } }
    val weight = mutableListOf<Int>()
    val purchase = mutableListOf<Int>()
    for (i in 0 until treasures.size) {
        weight += treasuresList[i].second.first
        purchase += treasuresList[i].second.second
    }
    if ((treasures.size == 1) && (weight[0] <= capacity)) answer.add(treasuresList[0].first)
    else{
        for (i in 1..treasures.size) {
            for (k in 1..capacity) {
                if (k >= weight[i - 1]) whole[i][k] =
                    maxOf(whole[i - 1][k], whole[i - 1][k - weight[i - 1]] + purchase[i - 1])
                else whole[i][k] = whole[i - 1][k]
            }
        }
        var cap = capacity
        var num = treasures.size
        while (num > 0) {
            if (whole[num][cap] != whole[num - 1][cap]) {
                answer.add(treasuresList[num - 1].first)
                cap -= weight[num]
            }
            num--
        }
    }
    return answer
}


fun myFun(text: String): Collection<Any> {
    val results = text.split(", ")
    val seconds = mutableListOf<Pair<String, Int>>()
    for (i in 0 until results.size) {
        val res = results[i].split(""" +""".toRegex())
        seconds += (res[0] to toSeconds(res[1]))
    }
    val sorted = seconds.sortedBy { it.first }.sortedBy { it.second }
    return sorted.map { it.first to fromSeconds(it.second) }
}

fun toSeconds(time: String): Int {
    val spl = time.trim().split(":")
    return spl[0].toInt() * 60 + spl[1].toInt()
}

fun fromSeconds(time: Int): String {
    val min = time / 60
    val sec = time % 60
    return String.format("%02d:%02d", min, sec)
}